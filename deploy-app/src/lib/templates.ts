/**
 * Opinionated scaffolding templates for docker-compose.prod.yml and Dockerfile.
 *
 * Every template is pre-wired to the Traefik + shared-infra setup that the
 * deploy CLI's setup-server installs: two external networks (`traefik`, `shared`),
 * a Let's Encrypt resolver, TLS on 443, a shared Postgres and Redis living on
 * the `shared` network that apps reach via env vars.
 */

export interface ScaffoldContext {
  appName: string;          // e.g. "sample-app-staging"
  envName: string;          // e.g. "staging"
  gitRepo: string | null;   // e.g. "acme/sample-app"
  domain: string | null;    // e.g. "staging.example.com"
  appPort: number;          // e.g. 3000
  extraContainers: string;  // space-separated
}

export type TemplateKind =
  | "node-api"
  | "python-api"
  | "go-api"
  | "static-frontend"
  | "fullstack";

export interface TemplateOption {
  kind: TemplateKind;
  label: string;
  description: string;
  languageHint: string;
}

export const templates: TemplateOption[] = [
  { kind: "node-api", label: "Node.js API", description: "Express / Fastify / NestJS — JSON API on a port.", languageHint: "JavaScript / TypeScript" },
  { kind: "python-api", label: "Python API", description: "FastAPI / Flask / Django — served by uvicorn/gunicorn.", languageHint: "Python" },
  { kind: "go-api", label: "Go API", description: "net/http / chi / gin — compiled static binary.", languageHint: "Go" },
  { kind: "static-frontend", label: "Static frontend", description: "Vite / CRA / SvelteKit static — served by nginx.", languageHint: "nginx" },
  { kind: "fullstack", label: "Full-stack (frontend + backend)", description: "Separate containers for your API and web UI, behind one domain.", languageHint: "mixed" },
];

