function image(ctx: ScaffoldContext, suffix = ""): string {
  const repo = ctx.gitRepo ?? "your-org/your-repo";
  return `ghcr.io/${repo}${suffix}:${ctx.envName}`;
}

function routerBlock(appName: string, domain: string | null, port: number, svc = "app"): string {
  if (!domain) {
    // Without a domain we can't do host-based routing. Emit a comment so the
    // user knows to fill it in later.
    return `      # No domain configured — add Traefik labels here once you have one:
      # - "traefik.enable=true"
      # - "traefik.http.routers.${appName}.rule=Host(\`your-domain.example\`)"
      # - "traefik.http.routers.${appName}.entrypoints=websecure"
      # - "traefik.http.routers.${appName}.tls.certresolver=letsencrypt"
      # - "traefik.http.services.${appName}.loadbalancer.server.port=${port}"`;
  }
  const suffix = svc === "app" ? "" : "-" + svc;
  return `      - "traefik.enable=true"
      - "traefik.http.routers.${appName}${suffix}.rule=Host(\`${domain}\`)"
      - "traefik.http.routers.${appName}${suffix}.entrypoints=websecure"
      - "traefik.http.routers.${appName}${suffix}.tls=true"
      - "traefik.http.routers.${appName}${suffix}.tls.certresolver=letsencrypt"
      - "traefik.http.services.${appName}${suffix}.loadbalancer.server.port=${port}"`;
}

function baseService(ctx: ScaffoldContext, svc = "app", port = ctx.appPort): string {
  return `  ${svc}:
    image: ${image(ctx, svc === "app" ? "" : "-" + svc)}
    container_name: ${ctx.appName}-${svc}
    restart: unless-stopped
    env_file:
      - .env
    networks:
      - proxy
      - shared-db
    labels:
${routerBlock(ctx.appName, ctx.domain, port, svc)}`;
}

export function generateCompose(kind: TemplateKind, ctx: ScaffoldContext): string {
  const header = `# Generated by Bishop for ${ctx.appName} (${ctx.envName}).
# This file is committed to your repo and scp'd to the server by ./deploy.
# Edit freely — Bishop will not overwrite without asking.

services:
`;
  const footer = `
networks:
  proxy:
    external: true
  shared-db:
    external: true
`;
  const extraNote = ctx.extraContainers.trim()
    ? `\n# Extra containers declared in .deploy/config: ${ctx.extraContainers}\n# Add them as additional services below, on the 'shared' network.\n`
    : "";

  switch (kind) {
    case "node-api":
    case "python-api":
    case "go-api":
      return header + baseService(ctx) + "\n" + extraNote + footer;

    case "static-frontend":
      // Nginx serves static assets on :80 inside the container; Traefik handles TLS.
      return header + baseService(ctx, "app", 80) + "\n" + extraNote + footer;

    case "fullstack": {
      // Two services: the API on <domain>/api and the web UI on <domain>/
      const apiDomain = ctx.domain ?? "example.com";
      const api = `  api:
    image: ${image(ctx, "-api")}
    container_name: ${ctx.appName}-api
    restart: unless-stopped
    env_file:
      - .env
    networks:
      - proxy
      - shared-db
    labels:
      - "traefik.enable=true"
      - "traefik.http.routers.${ctx.appName}-api.rule=Host(\`${apiDomain}\`) && PathPrefix(\`/api\`)"
      - "traefik.http.routers.${ctx.appName}-api.entrypoints=websecure"
      - "traefik.http.routers.${ctx.appName}-api.tls=true"
      - "traefik.http.routers.${ctx.appName}-api.tls.certresolver=letsencrypt"
      - "traefik.http.services.${ctx.appName}-api.loadbalancer.server.port=${ctx.appPort}"
`;
      const web = `  web:
    image: ${image(ctx, "-web")}
    container_name: ${ctx.appName}-web
    restart: unless-stopped
    networks:
      - proxy
    labels:
      - "traefik.enable=true"
      - "traefik.http.routers.${ctx.appName}-web.rule=Host(\`${apiDomain}\`)"
      - "traefik.http.routers.${ctx.appName}-web.entrypoints=websecure"
      - "traefik.http.routers.${ctx.appName}-web.tls=true"
      - "traefik.http.routers.${ctx.appName}-web.tls.certresolver=letsencrypt"
      - "traefik.http.services.${ctx.appName}-web.loadbalancer.server.port=80"
`;
      return header + api + "\n" + web + extraNote + footer;
    }
  }
}

