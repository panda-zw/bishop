export function generateDockerfile(kind: TemplateKind, ctx: ScaffoldContext): string {
  switch (kind) {
    case "node-api":
      return `# Generated by Bishop — Node.js API.
# Two-stage: install prod deps, then copy everything and run.

FROM node:20-slim AS deps
WORKDIR /app
COPY package*.json ./
RUN npm ci --omit=dev

FROM node:20-slim
WORKDIR /app
ENV NODE_ENV=production
COPY --from=deps /app/node_modules ./node_modules
COPY . .
EXPOSE ${ctx.appPort}
CMD ["node", "dist/server.js"]
`;

    case "python-api":
      return `# Generated by Bishop — Python API (uvicorn).
FROM python:3.12-slim

WORKDIR /app
RUN apt-get update && apt-get install -y --no-install-recommends gcc \\
    && rm -rf /var/lib/apt/lists/*

COPY requirements.txt .
RUN pip install --no-cache-dir -r requirements.txt

COPY . .

EXPOSE ${ctx.appPort}
CMD ["uvicorn", "main:app", "--host", "0.0.0.0", "--port", "${ctx.appPort}"]
`;

    case "go-api":
      return `# Generated by Bishop — Go API (compiled static binary).
FROM golang:1.23-alpine AS build
WORKDIR /src
COPY go.* ./
RUN go mod download
COPY . .
RUN CGO_ENABLED=0 GOOS=linux go build -ldflags='-s -w' -o /out/app ./cmd/app

FROM gcr.io/distroless/static-debian12
COPY --from=build /out/app /app
EXPOSE ${ctx.appPort}
ENTRYPOINT ["/app"]
`;

    case "static-frontend":
      return `# Generated by Bishop — static frontend served by nginx.
FROM node:20 AS build
WORKDIR /app
COPY package*.json ./
RUN npm ci
COPY . .
RUN npm run build

FROM nginx:alpine
COPY --from=build /app/dist /usr/share/nginx/html
# If your build output is at /app/build (CRA) or /app/.svelte-kit/output/client,
# change the COPY above accordingly.
EXPOSE 80
`;

    case "fullstack":
      return `# Generated by Bishop — full-stack API half.
# For the frontend, add a second Dockerfile (e.g. web/Dockerfile) and update
# docker-compose.prod.yml to build it, or publish it to GHCR under
# ghcr.io/<org>/<repo>-web:${ctx.envName}.

FROM node:20-slim AS deps
WORKDIR /app
COPY package*.json ./
RUN npm ci --omit=dev

FROM node:20-slim
WORKDIR /app
ENV NODE_ENV=production
COPY --from=deps /app/node_modules ./node_modules
COPY . .
EXPOSE ${ctx.appPort}
CMD ["node", "dist/server.js"]
`;
  }
}

