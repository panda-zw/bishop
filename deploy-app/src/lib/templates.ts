// Shared infrastructure (Traefik + shared Postgres + shared Redis).
// These three files live under .deploy/infra/shared/ and the CLI scp's them
// to /opt/shared/ on every deploy. Identical across projects that share
// the same server. Edit only if you know what you're doing.
// ---------------------------------------------------------------------------

export function generateSharedCompose(): string {
  return `# Generated by Bishop — shared infrastructure.
# Runs Traefik (reverse proxy + TLS), shared Postgres, and shared Redis.
# Each deployed app reaches these via the 'shared-db' network.

services:
  traefik:
    image: traefik:v3.1
    container_name: traefik
    restart: unless-stopped
    ports:
      - "80:80"
      - "443:443"
    volumes:
      - /var/run/docker.sock:/var/run/docker.sock:ro
      - ./traefik/traefik.yml:/etc/traefik/traefik.yml:ro
      - ./traefik/acme.json:/acme.json
    networks:
      - proxy

  shared-postgres:
    image: postgres:16
    container_name: shared-postgres
    restart: unless-stopped
    environment:
      POSTGRES_USER: \${POSTGRES_ADMIN_USER}
      POSTGRES_PASSWORD: \${POSTGRES_ADMIN_PASSWORD}
      POSTGRES_DB: postgres
    volumes:
      - /mnt/data/shared/postgres:/var/lib/postgresql/data
      - ./init-databases.sh:/docker-entrypoint-initdb.d/init-databases.sh:ro
    networks:
      - shared-db
    healthcheck:
      test: ["CMD-SHELL", "pg_isready -U $$POSTGRES_USER"]
      interval: 10s
      timeout: 5s
      retries: 5

  shared-redis:
    image: redis:7-alpine
    container_name: shared-redis
    restart: unless-stopped
    command: ["redis-server", "--requirepass", "\${REDIS_PASSWORD}", "--appendonly", "yes"]
    volumes:
      - /mnt/data/shared/redis:/data
    networks:
      - shared-db

networks:
  proxy:
    external: true
  shared-db:
    external: true
`;
}

export function generateSharedTraefik(acmeEmail = "admin@example.com"): string {
  return `# Generated by Bishop — Traefik static config.
# Static config changes require a Traefik restart; routing labels on each
# app service (set in their docker-compose.prod.yml) are picked up dynamically.

api:
  dashboard: false

entryPoints:
  web:
    address: ":80"
    http:
      redirections:
        entryPoint:
          to: websecure
          scheme: https
          permanent: true
  websecure:
    address: ":443"

providers:
  docker:
    exposedByDefault: false
    network: proxy

certificatesResolvers:
  letsencrypt:
    acme:
      email: ${acmeEmail}
      storage: /acme.json
      httpChallenge:
        entryPoint: web

log:
  level: INFO

accessLog: {}
`;
}

export function generateSharedInitDb(): string {
  return `#!/bin/bash
# Generated by Bishop — runs on first Postgres container init.
# Reads DATABASES env var (comma-separated) and creates each DB if absent.
# Apps can set POSTGRES_ADMIN_* in /opt/shared/.env and use the superuser
# to create per-app roles, or request a database via DATABASES env var.

set -e

if [ -z "\${DATABASES:-}" ]; then
  echo "[init-databases] No DATABASES env var set — skipping per-app DB creation."
  exit 0
fi

IFS=',' read -ra DBS <<< "\$DATABASES"
for db in "\${DBS[@]}"; do
  db=$(echo "$db" | xargs)
  [ -z "$db" ] && continue
  echo "[init-databases] Ensuring database: $db"
  psql -v ON_ERROR_STOP=1 --username "$POSTGRES_USER" <<-EOSQL
    SELECT 'CREATE DATABASE "$db"'
    WHERE NOT EXISTS (SELECT FROM pg_database WHERE datname = '$db')\\gexec
EOSQL
done
`;
}
